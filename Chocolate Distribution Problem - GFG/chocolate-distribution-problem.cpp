class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m)
    {
        sort(a.begin(),a.end());
        long long s=0,e=m-1,answer=INT64_MAX;
        while(e<n)
        {
            answer=min(a[e]-a[s],answer);
            s++;e++;
            
        }
        return answer;
    }   
};

