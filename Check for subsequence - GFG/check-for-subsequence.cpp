class Solution{
    public:
    bool isSubSequence(string A, string B) 
    {
        int l{0},h{0},n=A.length(),m=B.length();
        
        while(l<n && h<m)
        {
            if(B[h]==A[l]) l++;
                h++;
        }
        return (l==n);
        
    }
};

